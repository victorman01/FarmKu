def predict_disease(test_dir):
  list_test_image = os.listdir(test_dir)
  value=[]

  for list in list_test_image:
    image = os.path.join(test_dir, list)
    image = np.array(Image.open(image).resize((224, 224)).convert("RGB"))/255.0
    image = np.expand_dims(image, axis=0)
    predict = model.predict(image)
    print(predict)
    max_index = np.argmax(predict)
    if(max_index == 0):
        pred = "Anthracnose"
    elif(max_index == 1):
      pred = "Bacterial Canker"
    elif(max_index == 2):
      pred = "Cutting Weevil"
    elif(max_index == 3):
      pred = "Die Back"
    elif(max_index == 4):
      pred = "Gall Midge"
    elif(max_index == 5):
      pred = "Healthy"
    elif(max_index == 6):
      pred = "Powdery Mildew"
    elif(max_index == 7):
      pred = "Sooty Mould"

    true_pred = test_dir.split("/")[-1]

    if(pred == true_pred):
      value.append(1)
    else:
      value.append(0)
  result = print(f'Accuracy of predict {true_pred} was ' + str(np.mean(value)))
  return result

