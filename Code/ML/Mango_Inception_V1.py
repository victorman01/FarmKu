class stopEpoch(tf.keras.callbacks.Callback):
  def on_epoch_end(self, epoch, logs={}):
    if(logs.get('accuracy') >= 0.98 and logs.get('val_accuracy') > 0.90):
      print("\nThreshold achieve!\n")
      self.model.stop_training = True

